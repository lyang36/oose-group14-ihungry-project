/**
 * Store several signals for activity/screen transition
 * @author SuNFloWer
 *
 */
public class ActivitySwitchSignals {
	/**
	 * Request code for Mainscreen.
	 */
	static final public int MAINSCREEN = 101;
	
	/**
	 * Request code for Nearby.
	 */
	static final public int NEARBY = 102;
	
	/**
	 * Request code for Aboutme.
	 */
	static final public int ABOUTME = 103;
	
	/**
	 * Request code for Favourites.
	 */
	static final public int FAVOURITES = 104;
	
	/**
	 * Request code for OrderHistory.
	 */
	static final public int ORDERHISTORY = 105;
	
	/**
	 * Request code for Login.
	 */
	static final public int LOGIN = 106;
	
	/**
	 * Request code for OrderReview.
	 */
	static final public int ORDERREVIEW = 107;
	
	/**
	 * Request code for RestaurantInfo.
	 */
	static final public int RESTAURANTINFO = 108;
	
	
	
	/**
	 * View transition command to Quit.
	 */
	static final public int QUIT = 1000;
	
	/**
	 * View transition command to Mainscreen.
	 */
	static final public int MAINSCREENSWH = 1001;
	
	/**
	 * View transition command to Nearby.
	 */
	static final public int NEARBYSWH = 1002;
	
	/**
	 * View transition command to Aboutme.
	 */
	static final public int ABOUTMESWH = 1003;
	
	/**
	 * View transition command to Favourite.
	 */
	static final public int FAVOURITESSWH = 1004;
	
	/**
	 * View transition command to OrderHistory.
	 */
	static final public int ORDERHISTORYSWH = 1005;

	/**
	 * View transition command to Login.
	 */
	static final public int LOGINSWH = 1006;
	
	/**
	 * View transition command to close OrderReview.
	 */
	static final public int ORDERREVIEWCLOSESWH = 1007;
	
	/**
	 * View transition command to close RestaurantInfo.
	 */
	static final public int RESTAURANTINFOCLOSESWH = 1008;


}
